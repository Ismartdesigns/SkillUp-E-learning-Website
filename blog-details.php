                        <div class="blog-details__content">
                            <p>As our elementary students head back to school in person, in this very new way, there will be many emotions stirred up in them. Alarm. Frustration. Worry. Excitement. </p>
                            <p>And this will be mirrored by what we, as adults, may also be experiencing. For our teachers, on top of what they will be emotionally experiencing themselves, they are being called to be the caring leaders that guide our students to a place where they can learn together.</p>
                            <blockquote class="blockquote">
                                <p>This is going to be a challenging dance. Our teachers are true change makers. They are providers and they are leaders and this period in history is going to shine a light on their vital role in our children’s emotional health.</p>
                            </blockquote>
                            <p>So, how can we support them to support our children’s learning? As parents and school administrators, we can relax about the ‘learning’ and trust it will come. Schools are going to need to change the focus right now to concentrating on the emotional basics before academic basics. Teachers teach people, not subjects. And when they can focus on supporting well-being first, the learning may then have an opportunity to land. </p>
                            <p><strong>Let’s take a closer look at the 3 R’s of emotional basics:</strong></p>
                            <p><strong>Relationship</strong></p>
                            <p>What our students need from us is..us. They need to know we are there for them, and that they matter. It’s not so much about what we say—it’s about how we make them feel in our presence: invited, accepted, and seen. </p>
                            <p>During this emotionally turbulent time, we will need to make conscious invitations into relationship so that our students can feel connected to us. This might mean special greeting rituals at the beginning of each day and more playful activities in which we join in. These attachment practices can help our students to feel connected to us, which may also lower their anxiety. </p>
                            <p><strong>Rhythm</strong></p>
                            <p>Children crave rhythm.</p>
                            <p>Consistent routines, rituals, and structures help children feel safe. They can lean on these and rely on them. Yet most children are experiencing the exact opposite right now. And as they look to returning to school, they may have little to no sense of what the ‘new normal’ will be. We can create a sense of safety by quickly establishing new routines that our students can count on and orient around. This will help to produce a rhythm to their days and can offer a sense of predictability in these unpredictable times.</p>
                            <p><strong>Release</strong></p>
                            <p>Our students’ emotions will be stirred up. And we know that when emotions get stirred up, they need somewhere to go. Finding healthy ways to pre-emptively channel this emotional energy for our students can help to alleviate dangerous or disruptive eruptions. Integrating daily outlets for release can be especially helpful for supporting students to get out frustration before it leads to outbursts of aggression.</p>
                            <p>These outlets can also help students to reflect on and express their feelings in ways that don’t make them feel self-conscious. The beauty of this practice is that we don’t even have to know what is specifically going on for a child. We are simply facilitating a way for the emotion to be expressed and released indirectly in a natural way—whether through music, physical movement, stories or storytelling, writing, poetry, drama, art, or even simply being outdoors. All of these outlets are powerful because they help us come closer to our feelings and to experiencing a sense of release and emotional rest.</p>
                            <p>Going back to school during this time will not be easy. We will need to be creative and think outside the box. We may need to stretch muscles we never knew we had. But it may be helpful to remember that this is not a time to focus on outcome and performance, or getting ahead or even catching up. Shifting our attention to matters of the heart will help our students feel safe. This is what will set the stage for learning to happen – when children are ready.</p>
                            <p>In the meantime, let’s be patient with our students and ourselves. We are all in this together.</p>
